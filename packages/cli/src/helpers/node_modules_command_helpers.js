// @flow
const path = require('path');
const {dangerouslyCrossSpawnAsync} = require('./child_process_helpers');
const getBlocksCliProjectRootPath = require('./get_blocks_cli_project_root_path');

async function babelAsync(
    cwd: string,
    args: Array<string>,
): Promise<{stdout: string, stderr: string}> {
    return await dangerouslyCrossSpawnAsync(_getNodeModulesBinCommandPath('babel'), args, {
        cwd: cwd || process.cwd(),
        prefix: 'babel',
    });
}

async function npmAsync(
    cwd: string,
    args: Array<string>,
): Promise<{stdout: string, stderr: string}> {
    return await dangerouslyCrossSpawnAsync('npm', args, {
        cwd: cwd || process.cwd(),
        prefix: 'npm',
    });
}

async function gitAsync(
    cwd: string,
    args: Array<string>,
): Promise<{stdout: string, stderr: string}> {
    return await dangerouslyCrossSpawnAsync('git', args, {
        cwd: cwd || process.cwd(),
    });
}

function _getNodeModulesBinCommandPath(commandName: string): string {
    return path.join(getBlocksCliProjectRootPath(), 'node_modules', '.bin', commandName);
}

const nodeModulesCommandHelpers = {
    babelAsync,
    npmAsync,
    gitAsync,
};

module.exports = nodeModulesCommandHelpers;
