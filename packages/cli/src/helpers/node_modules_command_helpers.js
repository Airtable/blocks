// @flow
const path = require('path');
const {execFileAsync} = require('./child_process_helpers');
const getBlocksCliProjectRootPath = require('./get_blocks_cli_project_root_path');

async function _runNodeModulesCommandAsync(commandName: string, args: Array<string>, cwd: string | null = null): Promise<{stdout: string, stderr: string}> {
    const modulePath = path.join(getBlocksCliProjectRootPath(), 'node_modules', '.bin', commandName);
    return await execFileAsync(modulePath, args, {
        cwd: cwd || process.cwd(),
        prefix: commandName,
    });
}

async function yarnInstallAsync(cwd: string, args: Array<string>): Promise<{stdout: string, stderr: string}> {
    return await _runNodeModulesCommandAsync('yarn', args, cwd);
}

async function babelAsync(cwd: string, args: Array<string>): Promise<{stdout: string, stderr: string}> {
    return await _runNodeModulesCommandAsync('babel', args, cwd);
}

const nodeModulesCommandHelpers = {
    babelAsync,
    yarnInstallAsync,
};

module.exports = nodeModulesCommandHelpers;
