#!/usr/bin/env bash
set -euo pipefail

repository_root="$(cd "$(dirname "${BASH_SOURCE[0]}")/.." && pwd)"
cd "$repository_root"

# Declare all the directories to delete.
# The 'build' directory was renamed to 'transpiled', but adding 'build'
# here for backward compatibility to help cleanup the directory after upgrading
# to the codebase that uses the new 'transpiled' directory name.
declare -a directories_to_delete=(
    "${repository_root}/node_modules"
    "${repository_root}/block_backend_wrapper/node_modules"
    "${repository_root}/build"
    "${repository_root}/transpiled"
)

for directory_to_delete in "${directories_to_delete[@]}"; do
    echo "Wiping ${directory_to_delete}"
    rm -rf "${directory_to_delete}"
done

echo 'Done wiping directories.'
